package com.socialMedia.demo.model;

public class InteractionId {

    private Long postId;
    private Long userId;

    public InteractionId() {
    }

    public InteractionId(Long postId, Long userId) {
        this.postId = postId;
        this.userId = userId;
    }

    public Long getPostId() {
        return postId;
    }

    public void setPostId(Long postId) {
        this.postId = postId;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }
}
